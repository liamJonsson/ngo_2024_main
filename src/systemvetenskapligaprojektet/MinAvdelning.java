    private void laggInData(){
       
                try{
                    String selectAvdid = "select avdelning from anstalld where aid = (select aid from anstalld where epost = '" + inloggadAnvandare + "');";
                    String avdid = idb.fetchSingle(selectAvdid);
                    tfId.setText(avdid);
                    
                    String selectNamn = "select namn from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "');";
                    String namn = idb.fetchSingle(selectNamn);
                    tfNamn.setText(namn);
                    
                    String selectBeskrivning = "select beskrivning from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "');";
                    String beskrivning = idb.fetchSingle(selectBeskrivning);
                    tfBeskrivning.setText(beskrivning);
                    tfBeskrivning.setToolTipText(beskrivning);
                    
                    String selectAdress = "select adress from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "');";
                    String adress = idb.fetchSingle(selectAdress);
                    tfAdress.setText(adress);
                    
                    String selectEpost = "select epost from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "');";
                    String epost = idb.fetchSingle(selectEpost);
                    tfEpost.setText(epost);
                    
                    String selectTelefonnummer = "select telefon from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "');";
                    String telefonnummer = idb.fetchSingle(selectTelefonnummer);
                    tfTelefon.setText(telefonnummer);
                    
                    String selectStad = "select namn from stad where sid = (select stad from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "'));";
                    String stad = idb.fetchSingle(selectStad);
                    tfStad.setText(stad);
                    
                    String selectAvdelningschefF = "select fornamn from anstalld where aid = (select chef from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "'));";
                    String selectAvdelningschefE = "select efternamn from anstalld where aid = (select chef from avdelning where avdid = (select avdelning from anstalld where epost = '" + inloggadAnvandare + "'));";
                    String avdelningschefF = idb.fetchSingle(selectAvdelningschefF);
                    String avdelningschefE = idb.fetchSingle(selectAvdelningschefE);
                    String avdelningschef = avdelningschefF + " " + avdelningschefE;
                    tfAvdelningschef.setText(avdelningschef);

                    
                }
                catch(InfException ex){
                System.out.println(ex);
                
                
                
    }
    }
    
    

